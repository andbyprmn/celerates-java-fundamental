public class OperatorAritmatika {
      public static void main(String[] args) {
        int angka1;
        int angka2;
        int hasil;

       
        angka1 = 1;
        
        angka2 = 2;

        // penjumlahan
        hasil = angka1 + angka2;
        System.out.println("Hasil Tambah = " + hasil);

        

        // pengurangan
        hasil = angka1 - angka2;
        System.out.println("Hasil Kurang = " + hasil);

        

        // perkalian
        hasil = angka1 * angka2;
        System.out.println("Hasil Kali = " + hasil);


       
        // Pembagian
        hasil = angka1 / angka2;
        System.out.println("Hasil Bagi = " + hasil);

        

        // Sisa Bagi
        hasil = angka1 % angka2;
        System.out.println("Hasil Sisa = " + hasil);

    }
}
