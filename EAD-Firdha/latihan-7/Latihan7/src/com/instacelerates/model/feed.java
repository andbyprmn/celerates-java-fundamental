
package com.instacelerates.model;

public class feed {
    String fotoFeed;
    String captionFeed;
    int likeFeed;
    String commentsFeed;

    public String getFotoFeed() {
        return fotoFeed;
    }

    public void setFotoFeed(String fotoFeed) {
        this.fotoFeed = fotoFeed;
    }

    public String getCaptionFeed() {
        return captionFeed;
    }

    public void setCaptionFeed(String captionFeed) {
        this.captionFeed = captionFeed;
    }

    public int getLikeFeed() {
        return likeFeed;
    }

    public void setLikeFeed(int likeFeed) {
        this.likeFeed = likeFeed;
    }

    public String getCommentsFeed() {
        return commentsFeed;
    }

    public void setCommentsFeed(String commentsFeed) {
        this.commentsFeed = commentsFeed;
    }
    
    
}
